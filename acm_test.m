r1=0.999;
r2=0.99;
Fs=1000;
f1=304;
f2=85;
w1=2*pi*f1/Fs;
w2=2*pi*f2/Fs;
B1=[1 -r1*cos(w1)];
B2=[1 -r2*cos(w2)];
A1=[1 -2*r1*cos(w1) (r1*r1)];
A2=[1 -2*r2*cos(w2) (r2*r2)];
N=100;
x=[1;zeros(N-1,1)];
x=filter(B1,A1,x);
x=filter(B2,A2,x);
plot(1:length(x),x);
A_=acm(x,4);
figure(2);
[h,w]=freqz([1],A_,32,Fs);
plot(abs(w),h);
